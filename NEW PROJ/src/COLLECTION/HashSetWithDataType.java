package COLLECTION;

import java.util.HashSet;

public class HashSetWithDataType {
	
	public static void main(String[] args) {
		
		
HashSet <String> x = new HashSet<String>();
		
		x.add("123");
		x.add("c");
		x.add("Hello");
		
		// ADVANCE FOR LOOP
		
		for(String t: x) {
			
			System.out.println(t);
			
		}
		
		
		
		
	}

}
